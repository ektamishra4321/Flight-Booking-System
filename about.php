            <p class="one">History</p>
        </u>
        AeroFly was founded in 2003 as a joint venture between 2 companies.
        These made a bid in the mid-1990s to launch a full-service carrier in India that was unsuccessful,
        being denied regulatory approval by the Indian government. The two parent companies initially pledged to
        invest a combined US$100mn as start-up capital, one of the companies with 49% of the airline and the other 51%.
        AeroFly has recently added to its fleet the B737- MAX 8 aircraft which are newly designed, state-of-art aircraft
        with new fuel efficient engines.<br><br>

        AeroFly operates a fleet of Boeing and Q-400s that allow for greater efficiency in maintenance and to support
        its low-cost structure.<br><br>

        The new-generation Boeing 737-700s, 737-800s and 737-900ERs with winglets have been a conscious choice.
        These aircraft allow for safe, comfortable and efficient flying and are ideally suited for short to medium-haul
        flights typical of Indian conditions. Q400s, designed for short haul routes are known for their superlative fuel
        efficiency and comfort. These aircraft are the most technologically advanced turboprop airliners and are the
        next
        step in the continuing evolution of Indian aviation.
