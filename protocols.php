        <h2>For Airlines</h2>
    </div>
    <h3>
        <ol type="1">
            <li>
                Airline shall ensure that all passengers are wearing face mask properly and
                continue to wear the mask throughout the journey. The face masks can be removed
                under exceptional circumstances and for permitted reasons only.
            </li>
            <br>
            <li>
                The Airline shall ensure that passengers are sensitized on the precautionary
                measures to be taken by them through their websites, travel agents, call centers,
                display at airports, assistance booths at the airports, etc. =
                The Airline shall make regular announcements for awareness among the
                passengers on the need to follow COVID-19 appropriate behavior such as hand
                hygiene, wearing mask, etc. during travel. —
                The Airline shall make arrangement of extra Face Mask and provide to the
                passengers, if required.
            </li>
            <br>
            <li>
                The Airline shall ensure that in case any passenger does not adhere to above
                instructions even after repeated warnings, he/she should be de-boarded, if need be,
                before departure.
            </li>
            <br>
            <li>
                In case, any passenger on board an aircraft refuses to wear mask or violates the
                “COVID-19 Protocol for passengers” even after repeated warnings, during the
                course of the flight, such passenger may be treated as “Unruly Passenger” as
                defined in para 3.1 of Civil Aviation Requirements Section 3 Series M Part
                VI and the procedure in respect of handling such unruly
                passenger, as provided in above mentioned CAR, shall be followed by the
                concerned Airline.
            </li>
            <br>
    </h3>

    <div id="section2">
        <h2 style="margin-left:17px">For Airports</h2>
    </div>
    <ol type="1">
        <h3>
            <li>CISF or other Police personnel deployed at the entrance of the Airport shall ensure
                that no one is allowed to enter the Airport without wearing a mask. CASO and
                other supervising officers must ensure this personally.
            </li><br>
            <li>All Airport operators are hereby directed to increase airport announcements and
                surveillance to ensure that passengers at terminal are wearing Face Masks
                properly and follow COVID appropriate behavior at all times within the airport
                premises. In case any passenger does not wear mask or refuse to wear mask and
                follow COVID-19 protocol, he should be fined as per the respective State Law
                where the airport is located and he may even be handed over to Security Agencies
                for dealing with the matter as per the applicable law on the subject.
            </li><br>
            <li>Airport operators shall take sanitization measures at airports and provide hand
                sanitizers/dispensers at prominent places in the Terminal. Also, airport operators
                shall levy fines on those violating COVID protocols in accordance with state
                regulations with the help of local police/security agencies, etc. for violation of
                COVID Protocols.
            </li>
        </h3>
    </ol>

